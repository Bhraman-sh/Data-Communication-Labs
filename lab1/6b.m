X = [2 5; 4 6]
Y = [1 5; 6 -2]

disp("(a) Sum of X and Y")
X + Y

disp("(b) Difference of X and Y")
X - Y

disp("(c) Product of X and Y")
X * Y

disp("(d) Transpose of X")
X'

disp("(e) Inverse of X")
X ^ (-1)

disp("(f) Element by element multiplication of X and Y")
X .* Y

disp("(g) Element by element division of X by Y")
X ./ Y

disp("(h) Square of Each element of X")
X .^2

disp("(i) Raise Each element in X to the power by coresponding element in Y")
X .^ Y


